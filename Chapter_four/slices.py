foods = ['Biryani','Qurmo','Karahi','Mutton rosh','Dal Chawal']

print("The first three item in list are: ")
for food in foods[:3]:
    print(food)
print('\n')

print("The middle three item in list are: ")
for food in foods[1:4]:
    print(food)
print("\n")

print("The last three item in list are: ")
for food in foods[2:]:
    print(food)